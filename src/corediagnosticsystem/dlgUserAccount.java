    private void btnEditInfoEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditInfoEmployeeActionPerformed
        int rowSelected = tbUserAccount.getSelectedRow();
        if (rowSelected == -1) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No row selected. Please select a user from the table!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String employeeName = tbUserAccount.getValueAt(rowSelected, 1).toString();
        String username = tbUserAccount.getValueAt(rowSelected, 2).toString();
        if (!username.equals("")) {
            dlgUserUpdateRole update = new dlgUserUpdateRole(main, this, true, username,employeeName);
            SwingUtils.fadeIn(update);
        }
    }//GEN-LAST:event_btnEditInfoEmployeeActionPerformed

