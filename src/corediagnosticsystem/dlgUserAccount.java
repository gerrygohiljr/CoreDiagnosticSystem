    public void showUserInfo() {
        DefaultTableModel model = (DefaultTableModel) tbUserAccount.getModel();
        int row = 0;
        DBQueries db = new DBQueries();
        try {
            CommonMethod.clearTable(tbUserAccount);
            db.ConnectToDatabase();
            db.queryUsersAccount();
            while (db.rs.next()) {
                model.insertRow(row++, new Object[]{row, db.rs.getString("emp_lastname") + ", " + db.rs.getString("emp_firstname") + " " + db.rs.getString("emp_extension") + " " + db.rs.getString("emp_middlename"), db.rs.getString("username"), db.rs.getString("role"), db.rs.getString("status"), db.rs.getString("date_created")});
            }
        } catch (SQLException e) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (db.con != null) {
                db.closeConnection();
            }

        }
    }

