    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String labID = txtLabID.getText().trim();
        String category = cmbCategory.getSelectedItem().toString();
        String labname = txtName.getText().trim();
        String amount = txtAmount.getText().trim();
        String status = cmbStatus.getSelectedItem().toString();
        boolean view = cmbView.getSelectedItem().toString().equals("DISPLAY");
        if (category.equals("-SELECT-")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No Category Selected. Please select Laboratory Category!", "ERROR", JOptionPane.ERROR_MESSAGE);
            cmbCategory.requestFocus();
            return;
        }
        if (labname.equals("")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No Laboratory Name found. Please enter laboratory name!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtName.requestFocus();
            return;
        }
        if (amount.equals("")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No Laboratory Amount found. Please enter Laboratory Amount!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtAmount.requestFocus();
            return;
        }
        double parseAmount = Double.parseDouble(amount);
        DBQueries db = new DBQueries();
        try {
            int CONFIRM = JOptionPane.showConfirmDialog(this, "Are all information correct?\n\n"
                    + "Lab. ID: " + labID + "\n"
                    + "Lab. Category: " + category + "\n"
                    + "Lab. Name:     " + labname + "\n"
                    + "Lab. Amount:   " + amount + "\n"
                    + "Lab. Status:   " + status + "\n"
                    + "MedTech. View:   " + cmbView.getSelectedItem().toString() + "\n\n", "CONFIRM", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (CONFIRM == JOptionPane.YES_OPTION) {
                db.ConnectToDatabase();
                /*
                boolean exist = db.checkExistingLabInfo(labname);
                if (exist) {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, labname + " already exist in the database record!", "ERROR", JOptionPane.ERROR_MESSAGE);
                    txtName.requestFocus();
                    return;
                }
                */
                int i = db.updateLaboratoryInformation(labID,category, labname, parseAmount, status,view);
                if (i > 0) {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(null, "Updated Successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    main.showLoading(GlobalVariables.FORM_LABORATORY, "Refreshing Laboratory Records....");
                } else {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(null, "Update Failed!", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (HeadlessException e) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (db.con != null) {
                db.closeConnection();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

