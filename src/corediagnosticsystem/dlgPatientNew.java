    private void AutoComplete() {

        DBQueries db = new DBQueries();
        try {
            db.ConnectToDatabase();
            JTextField lname = txtLastname;
            //call autocomplete method
            AutoTextComplete autolname = new AutoTextComplete(lname);
            AutoTextComplete autofname = new AutoTextComplete(txtFirstname);
            AutoTextComplete automname = new AutoTextComplete(txtMiddlename);
            AutoTextComplete autoename = new AutoTextComplete(txtExtension);
            AutoTextComplete autoaddress = new AutoTextComplete(txtAddress);
            new AutoCompleteDBLink(autolname, "select distinct lastname from patient_tb order by lastname;", db.con);
            new AutoCompleteDBLink(autofname, "select distinct firstname from patient_tb order by firstname;", db.con);
            new AutoCompleteDBLink(automname, "select distinct middlename from patient_tb order by middlename;", db.con);
            new AutoCompleteDBLink(autoename, "select distinct extension from patient_tb order by extension;", db.con);
            new AutoCompleteDBLink(autoaddress, "select distinct address from patient_tb order by address;", db.con);

        } catch (Exception e) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (db.con != null) {
                db.closeConnection();
            }

        }
    }

