    private void lblChangeAutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblChangeAutoMouseClicked
        if (!lblPatientID.isEnabled() && lblChangeAuto.getText().equals("<html><b><u>Change..</u></b></html>")) {
            lblPatientID.setEnabled(true);
            lblPatientID.requestFocusInWindow();
            lblChangeAuto.setText("<html><b><u>Auto..</u></b></html>");
        } else {
            CommonMethod.getAutoPatientID(lblPatientID);
            lblChangeAuto.setText("<html><b><u>Change..</u></b></html>");
            lblPatientID.setEnabled(false);
        }
    }//GEN-LAST:event_lblChangeAutoMouseClicked

