    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String lname = txtLastname.getText().trim();
        String fname = txtFirstname.getText().trim();
        String mname = txtMiddlename.getText().trim();
        String ext = txtExtension.getText().trim();
        String gender = cmbGender.getSelectedItem().toString();
        Date bdate = dcBirthDate.getDate();
        
        String position = cmbPosition.getSelectedItem().toString();
        String licenseno = txtLicenseNo.getText().trim();
        String status = cmbEmploymentStatus.getSelectedItem().toString();
        String address = txtAddress.getText().trim();
        String contact = txtCellphoneNo.getText().trim();
        String employeeID = lblEmployeeID.getText().trim();
        
        if (employeeID.equals("####-######")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No employee ID generated. Please contact the System Administrator for help!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtLastname.requestFocus();
            return;
        }
        if (lname.equals("")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No lastname found. Please enter employee lastname!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtLastname.requestFocus();
            return;
        }
        if (fname.equals("")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No firstname found. Please enter employee firstname!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtFirstname.requestFocus();
            return;
        }
        if (gender.equals("-SELECT-")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No gender selected. Please select employee gender!", "ERROR", JOptionPane.ERROR_MESSAGE);
            cmbGender.requestFocus();
            return;
        }
        if (bdate == null) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No birth date found. \nPlease enter employee birth date! or enter a valid birthdate! ex. April 7, 1990", "ERROR", JOptionPane.ERROR_MESSAGE);
            dcBirthDate.requestFocus();
            return;
        }
        if (bdate.after(new java.util.Date())) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Invalid birth date found. Please enter employee valid birthdate ex. April 7, 1990!", "ERROR", JOptionPane.ERROR_MESSAGE);
            dcBirthDate.requestFocus();
            return;
        }
        
         if (position.equals("-SELECT-")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No position selected. Please select employee position!", "ERROR", JOptionPane.ERROR_MESSAGE);
            cmbPosition.requestFocus();
            return;
        }
         if (status.equals("-SELECT-")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No employment status selected. Please select employee status!", "ERROR", JOptionPane.ERROR_MESSAGE);
            cmbEmploymentStatus.requestFocus();
            return;
        }
        if (address.equals("")) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No address found. Please enter employee address!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtAddress.requestFocus();
            return;
        }
        if (!contact.equals("")) {
            System.out.println(contact.length());
            if (contact.length() != 11) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Invalid cellphone no. found. Please enter 11-digits cellphone no.!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtLicenseNo.requestFocus();
                return;
            }
        }
        if(UPLOADED==false){
             Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "No image uploaded found. Please upload employee picture!", "ERROR", JOptionPane.ERROR_MESSAGE);
            btnUpload.requestFocus();
            return;
        }
        PreparedStatement ps = null;
        DBQueries db = new DBQueries();
        try {
            int CONFIRM = JOptionPane.showConfirmDialog(this, "Are all information correct?", "CONFIRM", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (CONFIRM == JOptionPane.YES_OPTION) {
                db.ConnectToDatabase();
                boolean exist = db.checkExistingEmployeeInfo(lname, fname, mname);
                if (exist) {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, lname + ", " + fname + " " + mname + " already exist in the database record!", "ERROR", JOptionPane.ERROR_MESSAGE);
                    txtLastname.requestFocus();
                    return;
                }
                ps = db.con.prepareStatement("INSERT INTO employee_tb(employee_id,emp_lastname,emp_firstname,emp_middlename,emp_extension,emp_birthdate,"
                        + "emp_gender,position_id,license_no,emp_address,emp_cellphoneno,emp_status,date_registered,image) "
                        + "VALUES(?,?,?,?,?,'"+bdate+"',?,"
                        + "(SELECT position_id from position_tb WHERE position_name=?),?,?,?,?,now(),?)");
                //ps = db.con.prepareStatement("INSERT INTO employee_tb VALUES('" + employeeID + "','" + lnamse + "','" + fname + "','" + mname + "','" + ext + "','" + bdate + "','" + gender + "',(SELECT position_id from position_tb WHERE position_name='" + position + "'),'" + licenseno + "','" + address + "','" + contact + "','" + status+ "','" + username + "',md5('" + password + "'),now())");
                ps.setString(1, employeeID);
                ps.setString(2, lname);
                ps.setString(3, fname);
                ps.setString(4, mname);
                ps.setString(5, ext);
                ps.setString(6, gender);
                ps.setString(7, position);
                ps.setString(8, licenseno);
                ps.setString(9, address);
                ps.setString(10, contact);
                ps.setString(11, status);
                ps.setBinaryStream(12, fin, (int) file.length());
                int i=ps.executeUpdate();
                
                 //= db.insertEmployeeInformation(employeeID, lname, fname, mname, ext, gender, bdate,username,password,position,licenseno,status, address, contact);
                if (i > 0) {
                    INSERT = true;
                    UPLOADED = false;
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(null, "Inserted Successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    int CONFIRM2 = JOptionPane.showConfirmDialog(this, "Do you want to add more employee info?", "CONFIRM", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (CONFIRM2 == JOptionPane.YES_OPTION) {
                        CommonMethod.getAutoEmployeeID(lblEmployeeID);
                        ClearFields();
                    } else {
                        this.dispose();
                        main.showLoading(GlobalVariables.FORM_EMPLOYEES, "Refreshing Employee Records....");
                    }

                } else {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(null, "Insert Failed! Please contact the System Administrator for asssisstance and help.", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (HeadlessException | SQLException e) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (db.con != null) {
                db.closeConnection();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

