    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
       int height = lblImage.getHeight() - 2;
        int width = lblImage.getWidth();
        
        if (fc == null) {
            fc = new JFileChooser();
            fc.setCurrentDirectory(new java.io.File("../.././Picture"));
            //Add a custom file filter and disable the default
            //(Accept All) file filter.
            fc.addChoosableFileFilter(new ImageFilter());
            fc.setAcceptAllFileFilterUsed(false);
            //Add custom icons for file types.
            fc.setFileView(new ImageFileView());
            //Add the preview pane.
            fc.setAccessory(new ImagePreview(fc));
        }
        //Show it.
        int returnVal = fc.showDialog(this, "Select Image");
        //Process the results.
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            file = fc.getSelectedFile();
            if (file.isFile()) {
                try {
                    fin = new FileInputStream(file);
                    ImageIcon image = new ImageIcon(file.getAbsolutePath());
                    Image img = image.getImage();
                    Image newimg = img.getScaledInstance(width, height, java.awt.Image.SCALE_DEFAULT);
                    lblImage.setText("");
                    lblImage.setIcon(new ImageIcon(newimg));
                    String tooltip = "<html>" + "<center>" + "" + "<img height=\"500\" width=\"500\" src=\"file:" + file + "\">" + "</center></html>";
                    lblImage.setToolTipText(tooltip);
                    btnUpload.setText("CHANGE IMAGE");
                    UPLOADED = true;
                } catch (Exception e) {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                }
                //studnumber.setText(file.getName().substring(0,file.getName().length()-4));
            }

        }
        //Reset the file chooser for the next time it's shown.
        fc.setSelectedFile(null);
    }//GEN-LAST:event_btnUploadActionPerformed

