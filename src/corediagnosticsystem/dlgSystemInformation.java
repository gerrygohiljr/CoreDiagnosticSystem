    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (btnSave.getText().equals("EDIT")) {
            txtCompanyName.setEnabled(true);
            txtLocation.setEnabled(true);
            txtProprietorName.setEnabled(true);
            txtContactNo.setEnabled(true);
            txtEmailAdd.setEnabled(true);
            txtSystemVersionNo.setEnabled(true);
            txtCompanyName.requestFocus();
            btnSave.setText("SAVE");
        } else {
            String companyName = txtCompanyName.getText().trim();
            String location = txtLocation.getText().trim();
            String propreitor = txtProprietorName.getText().trim();
            String contactno = txtContactNo.getText().trim();
            String emailadd = txtEmailAdd.getText().trim();
            String versionno = txtSystemVersionNo.getText().trim();
            if (companyName.equals("")) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No Company name found. Please enter company name!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtCompanyName.requestFocus();
                return;
            }
            if (location.equals("")) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No Location found. Please enter Location!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtLocation.requestFocus();
                return;
            }
            if (propreitor.equals("")) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No Proprietor Name found. Please enter Proprietor Name!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtProprietorName.requestFocus();
                return;
            }
            if (contactno.equals("")) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No Contact Number found. Please enter Contact Number!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtContactNo.requestFocus();
                return;
            }
            if (emailadd.equals("")) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No Email Address found. Please enter Email Address!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtEmailAdd.requestFocus();
                return;
            }
            if (versionno.equals("")) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No System Version No. found. Please enter System Version No.!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtSystemVersionNo.requestFocus();
                return;
            }

            DBQueries db = new DBQueries();
            int task = 0;
            try {
                int CONFIRM = JOptionPane.showConfirmDialog(this, "Are all information correct?", "CONFIRM", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (CONFIRM == JOptionPane.YES_OPTION) {
                    db.ConnectToDatabase();
                    db.con.setAutoCommit(false);
                    db.deleteSystemInfo();
                    task += db.insertSystemInformation(GlobalVariables.COMPANY_NAME, companyName);
                    task += db.insertSystemInformation(GlobalVariables.COMPANY_LOCATION, location);
                    task += db.insertSystemInformation(GlobalVariables.COMPANY_PROPRIETOR_NAME, propreitor);
                    task += db.insertSystemInformation(GlobalVariables.COMPANY_CONTACT_NO, contactno);
                    task += db.insertSystemInformation(GlobalVariables.COMPANY_EMAIL_ADD, emailadd);
                    task += db.insertSystemInformation(GlobalVariables.COMPANY_SYSTEM_VERSION, versionno);
                    if (task > 5) {
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(null, "Saved Successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                        btnSave.setText("EDIT");
                        txtCompanyName.setEnabled(false);
                        txtLocation.setEnabled(false);
                        txtProprietorName.setEnabled(false);
                        txtContactNo.setEnabled(false);
                        txtEmailAdd.setEnabled(false);
                        txtSystemVersionNo.setEnabled(false);
                    } else {
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(null, "Save Failed!", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                }

            } catch (HeadlessException | SQLException e) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                try {
                    if (db.con != null) {
                        db.con.rollback();
                    }
                } catch (SQLException ex) {
                }
            } finally {
                if (db.con != null) {
                    try {
                        db.con.commit();
                        db.closeConnection();
                    } catch (SQLException ex) {
                    }

                }

            }

        }


    }//GEN-LAST:event_btnSaveActionPerformed

