    private void checkSystemInfo() {

        DBQueries db = new DBQueries();
        try {
            db.ConnectToDatabase();
            db.querySystemInfo();
            while (db.rs.next()) {
                if (db.rs.getString("attributes").equals(GlobalVariables.COMPANY_NAME)) {
                    txtCompanyName.setText(db.rs.getString("values"));
                    this.setTitle(db.rs.getString("values"));
                } else if (db.rs.getString("attributes").equals(GlobalVariables.COMPANY_LOCATION)) {
                    txtLocation.setText(db.rs.getString("values"));
                } else if (db.rs.getString("attributes").equals(GlobalVariables.COMPANY_PROPRIETOR_NAME)) {
                    txtProprietorName.setText(db.rs.getString("values"));
                } else if (db.rs.getString("attributes").equals(GlobalVariables.COMPANY_CONTACT_NO)) {
                    txtContactNo.setText(db.rs.getString("values"));
                } else if (db.rs.getString("attributes").equals(GlobalVariables.COMPANY_EMAIL_ADD)) {
                    txtEmailAdd.setText(db.rs.getString("values"));
                } else if (db.rs.getString("attributes").equals(GlobalVariables.COMPANY_SYSTEM_VERSION)) {
                    txtSystemVersionNo.setText(db.rs.getString("values"));
                    this.setTitle(this.getTitle()+" Ver. "+db.rs.getString("values"));
                }
            }
            txtCompanyName.setEnabled(false);
            txtLocation.setEnabled(false);
            txtProprietorName.setEnabled(false);
            txtContactNo.setEnabled(false);
            txtEmailAdd.setEnabled(false);
            txtSystemVersionNo.setEnabled(false);
            btnSave.setText("EDIT");
        } catch (SQLException e) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (db.con != null) {
                db.closeConnection();
            }

        }
    }

